package org.lianxi.entity;

public class Role {
      private int id;
      private String name;
      private boolean ststus;
      
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public boolean isStstus() {
		return ststus;
	}
	public void setStstus(boolean ststus) {
		this.ststus = ststus;
	}
      
      
}
